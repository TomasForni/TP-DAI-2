const num1 = 7, num2 = 4, num3 = 23;
const CantNum = 3; 

function SumaTotal(){
    let = SumaNumeros;
    SumaNumeros = num1 + num2 + num3;
    return SumaNumeros;
}

function RestaTotal(){
    let = RestaNumeros;
    RestaNumeros = num1 - num2 - num3;
    return RestaNumeros;
}

function Promedio(){
    let Promedio;
    Promedio = num1 + num2 + num3;
    Promedio = Promedio % CantNum;
    return Promedio;
}

